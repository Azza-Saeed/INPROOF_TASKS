#include "STD_Types.h"
#include "Bit_Math.h"

#include "GIE.h"

#define SREG    *((volatile u8*)0x5F)
/******************************************************************************
* \Syntax          : void GIE_voidGlobalInterruptEnable(void)      
* \Description     : this service for enable interrupt                                                                                                         
* \Return value:   : None                            
*******************************************************************************/
void GIE_voidGlobalInterruptEnable(void)
{
	SET_BIT(SREG,7);
}

/******************************************************************************
* \Syntax          : void GIE_voidGlobalInterruptDisable(void)      
* \Description     : this service for diable interrupt                                                                                                               
* \Return value:   : None                            
*******************************************************************************/
void GIE_voidGlobalInterruptDisable(void)
{
	CLEAR_BIT(SREG,7);
}
